package com.ubs.omnia.tools.db2csv.exception;


public class GenericReaderException extends GenericConverterException {

	private static final long serialVersionUID = 1L;

	public GenericReaderException() {
		super();
	}

	public GenericReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public GenericReaderException(String message) {
		super(message);
	}

	public GenericReaderException(Throwable cause) {
		super(cause);
	}

}
